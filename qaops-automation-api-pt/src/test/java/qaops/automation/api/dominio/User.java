package qaops.automation.api.dominio;

public class User {
	private String name;
	private String job;
	
	public User() {}
	
	public User(String name, String job) {
		super();
		this.name = name;
		this.job = job;
	}

	public String getName() {
		return name;
	}


	public String getJob() {
		return job;
	}

	
	
	
	
	
}
